                                    <li><strong>Contaminants that harm.</strong> Herbal raw materials and finished products can be contaminated with heavy metals (lead, mercury, arsenic), pesticide residues, microbial pathogens, aflatoxins, and inorganic adulterants. These contaminants can cause acute or chronic toxicity independent of the herbal pharmacology. WHO and multiple reviews emphasize monitoring for these contaminants as a primary safety measure. <a href="https://www.who.int/docs/default-source/medicines/norms-and-standards/guidelines/quality-control/quality-control-methods-for-medicinal-plant-materials.pdf?sfvrsn=b451e7c6_0&utm_source=chatgpt.com" target="_blank"> World Health Organization+1 <i class="fa-solid fa-arrow-up-right-from-square"></i></a> </li>

