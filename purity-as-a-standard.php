                            <div class="approach-tab-list full mt-10">
                                <ul>
                                    <li><strong>Identity → consistent chemistry.</strong> Plants vary by species, chemovar, geography, harvest time and processing. If raw material is misidentified or substituted, the phytochemical profile (and therefore efficacy) changes. Standardized identity checks (macroscopy, microscopy, DNA barcoding) and chemical fingerprinting (HPLC, GC-MS) keep the active profile consistent. <a href="https://www.who.int/docs/default-source/medicines/norms-and-standards/guidelines/quality-control/quality-control-methods-for-medicinal-plant-materials.pdf?sfvrsn=b451e7c6_0&utm_source=chatgpt.com" target="_blank"> World Health Organization+1 <i class="fa-solid fa-arrow-up-right-from-square"></i></a> </li>

                                    <li><strong>Removal of interfering impurities.</strong> In some cases, non-target impurities (oxidized compounds, tannins, residual solvents) mask or alter the bioactivity of the intended actives. Purification improves the signal-to-noise ratio of desired constituents and thus the product’s performance. <a href="https://www.sciencedirect.com/science/article/pii/S2666831921000400?utm_source=chatgpt.com" target="_blank"> ScienceDirect <i class="fa-solid fa-arrow-up-right-from-square"></i></a></li>
                                </ul>
                            </div>
